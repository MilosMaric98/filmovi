let container = document.querySelector(".container");

for (let i = 0; i < movies[0].name.length; i++) {
  let item = document.createElement("div");
  item.classList.add("item");
  let movieName = document.createElement("p");
  movieName.classList.add("movie_name");
  movieName.textContent = movies[0].name[i];
  let movie = document.createElement("div");
  movie.classList.add("movie");
  let link = document.createElement("a");
  link.classList.add("link");
  link.setAttribute("href", "playMovie.html");
  let image = document.createElement("img");
  image.classList.add("image");
  image.src = movies[0].imageUrl[i];
  let overlay = document.createElement("div");
  overlay.classList.add("overlay");
  let text = document.createElement("p");
  text.classList.add("text");
  text.textContent = movies[0].desc[i];

  overlay.appendChild(text);
  link.appendChild(image);
  link.appendChild(overlay);
  movie.appendChild(link);
  item.appendChild(movieName);
  item.appendChild(movie);
  container.appendChild(item);
}

document.getElementById("zanr").onchange = function () {
  let selectField = document.getElementById("zanr");
  let selectedOption = selectField.options.selectedIndex;
  let selectedValue = selectField.options[selectedOption].innerHTML;

  let movieName = document.querySelectorAll(".movie_name");
  let image = document.querySelectorAll(".image");
  let text = document.querySelectorAll(".text");

  //console.log(selectedValue);
  for (let i = 0; i < movies[0].name.length; i++) {
    //console.log(movies[2].name[i]);
    if (selectedValue === "Akcija") {
      movieName[i].textContent = movies[0].name[i];
      image[i].src = movies[0].imageUrl[i];
      text[i].textContent = movies[0].desc[i];
    } else if (selectedValue === "Komedija") {
      movieName[i].textContent = movies[1].name[i];
      image[i].src = movies[1].imageUrl[i];
      text[i].textContent = movies[1].desc[i];
    } else if (selectedValue === "Drama") {
      movieName[i].textContent = movies[2].name[i];
      image[i].src = movies[2].imageUrl[i];
      text[i].textContent = movies[2].desc[i];
    } else if (selectedValue === "Naučna fantastika") {
      movieName[i].textContent = movies[3].name[i];
      image[i].src = movies[3].imageUrl[i];
      text[i].textContent = movies[3].desc[i];
    }
  }
};

